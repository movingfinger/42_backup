int		ft_atoi(char *str)
{
	int		i;
	int		n;
	int		sig;
	char	*s;

	i = 0;
	n = 0;
	sig = 1;
	while (str[i] != '\0')
	{
		s = str[i];
		if ((str[i] == '-') && (str[i + 1] >= 48 && str[i + 1] <= 57))
			sig = -1;
		else if (str[i] >= 48 && str[i] <= 57)
			n = n * 10 + (str[i] - '0');
		else if (!((s >= 9 && s <= 13) || s == 32 || s == 43))
			return (n * sig);
		i++;
	}
	return (n * sig);
}
