int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int n;

	i = 0;
	n = 0;
	if (s1[0] == '\0')
		return (-s2[0]);
	else if (s2[0] == '\0')
		return (s1[0]);
	else
	{
		while ((s1[i] != '\0') && s2[i] != '\0')
		{
			n += (s1[i] - s2[i]);
			i++;
		}
	}
	return (n);
}
