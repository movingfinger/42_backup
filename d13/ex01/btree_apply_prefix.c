#include "ft_btree.h"

void	btree_apply_prefix(t_btree *root, void (*applyf)(void *))
{
	if (root)
	{
		applyf(root->item);
		btree_apply_prefix(root, applyf(root->left));
		btree_apply_prefix(root, applyf(root->right));
	}
	return ;
}
