int		ft_atoi(char *str)
{
	int		neg;
	int		i;
	int		num;

	neg = 1;
	i = 0;
	num = 0;
	while (str[i] <= 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg *= -1;
		i++;
	}
	if (!(str[i] >= 48 && str[i] <= 57))
		return (0);
	while ((str[i] >= 48 && str[i] <= 57))
		num = num * 10 + (str[i++] - '0');
	return (num * neg);
}
