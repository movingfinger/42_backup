#include "ft_colle2.h"

int		main(int ac, char **av)
{
	int		x;
	int		y;

	if (ac == 3)
	{
		x = ft_atoi(av[1]);
		y = ft_atoi(av[2]);
		if (x <= 0 || y <= 0)
			return (0);
		else
			ft_putstr(rush02(x, y));
	}
	return (0);
}
