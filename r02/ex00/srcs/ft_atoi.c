#include "ft_colle2.h"

int		ft_atoi(char *str)
{
	int		i;
	int		n;
	int		neg;

	i = 0;
	n = 0;
	neg = 1;
	while (str[i] != '\0')
	{
		if (str[i] == '-' || str[i] == '+')
		{
			if (str[i] == '-')
				neg *= -1;
			i++;
		}
		if (str[i] <= 32)
			i++;
		if (!(str[i] >= '0' && str[i] <= '9'))
			return (0);
		if (str[i] >= '0' && str[i] <= '9')
			n = n * 10 + (str[i] - '0');
		i++;
	}
	return (n * neg);
}
