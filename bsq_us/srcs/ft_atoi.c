#include "ft_print.h"

int		ft_atoi(char *str)
{
	int		i;
	int		num;
	int		neg;

	i = 0;
	num = 0;
	neg = 1;
	while (str[i] < 33)
	{
		if (str[i] == '-' || str[i] == '+')
		{
			if (str[i] == '-')
			{
				ft_putchar('-');
				neg *= -1;
			}
			i++;
		}
		if (!(str[i] >= '0' && (str[i] <= '9')))
			return (0);
		if (str[i] >= '0' && (str[i] <= '9'))
			num = num * 10 + (str[i] - '0');
		i++;
	}
	return (num * neg);
}
