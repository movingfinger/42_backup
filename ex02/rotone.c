#include <unistd.h>

void	ft_rotone(char c, int n)
{
	if (c >= 'a' && c <= 'z')
		c = (c - 'a' + n) % 26 + 'a';
	else if (c >= 'A' && c <= 'Z')
		c = (c - 'A' + n) % 26 + 'Z';
	write (1, &c, 1);
}

int		main(int argc, char **argv)
{
	int		i;

	i = 0;
	if (argc == 2)
		while (argv[1][i] != '\0')
			ft_rotone(argv[1][i++], 42);
	write(1, "\n", 1);
	return (0);
}
