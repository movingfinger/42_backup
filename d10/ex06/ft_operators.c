int		ft_plus(int n1, int n2)
{
	return (n1 + n2);
}

int		ft_minus(int n1, int n2)
{
	return (n1 - n2);
}

int		ft_multiply(int n1, int n2)
{
	return (n1 * n2);
}

int		ft_divide(int n1, int n2)
{
	return (n1 / n2);
}

int		ft_mod(int n1, int n2)
{
	return (n1 % n2);
}
