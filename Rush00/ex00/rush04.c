void	ft_putchar(char c);

void	rush(int x, int y)
{
	int v;
	int h;
	int c;

	v = 0;
	h = 0;
	c = x != 1 && y != 1;
	while (v < y)
	{
		while (h < x)
		{
			if ((h == 0 && v == 0) || (c && (h == (x - 1) && v == (y - 1))))
				ft_putchar('A');
			else if ((h == 0 && v == (y - 1)) || (h == (x - 1) && v == 0))
				ft_putchar('C');
			else if ((h != 0 && h != (x - 1)) && (v != 0 && v != (y - 1)))
				ft_putchar(' ');
			else
				ft_putchar('B');
			h++;
		}
		v++;
		ft_putchar('\n');
		h = 0;
	}
}
