#include <stdio.h>
char	*ft_rot42(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 97) && (str[i] <= 122))
		{
			str[i] = str[i] + 17;
			if (str[i] > 122)
				str[i] = str[i] - 25;
		}
		else if ((str[i] >= 65) && (str[i] <= 90))
		{
			str[i] += 17;
			if (str[i] > 90)
				str[i] -= 25;
		}
		i++;
	}
	return (str);
}
