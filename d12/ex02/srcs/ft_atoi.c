int		ft_atoi(char *str)
{
	int		i;
	int		neg;
	int		n;

	i = 0;
	neg = 1;
	n = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '-' || str[i] == '+')
		{
			if (str[i] == '-')
				neg *= -1;
			i++;
		}
		if (str[i] < 33)
			i++;
		if (!(str[i] > 47 && str[i] < 58))
			return (0);
		if (str[i] > 47 && str[i] < 58)
			n = n * 10 + (str[i] - '0');
		i++;
	}
	return (n * neg);
}
